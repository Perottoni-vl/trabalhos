#Principais Tipos de Estruturas Condicionais:

#1) Condicional Simples (if): Executa um bloco de código apenas se a condição for verdadeira.
#Exemplo: if (idade >= 18) { printf("Maior de idade"); }

#2) Condicional Composta (if-else): Seleciona um entre dois grupos de ações. Se a condição for falsa, 
#o bloco else é executado.
# Exemplo: if (a > b) { ... } else { ... }

#3) Condicional Encadeada/Aninhada (if-else if-else): Verifica múltiplas condições sucessivamente até encontrar uma verdadeira.
    
#4) Seleção de Múltipla Escolha (switch-case): Mais indicada para comparar uma variável contra vários valores constantes,
#sendo mais organizada que múltiplos if-else

